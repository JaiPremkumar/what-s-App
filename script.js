function send(){
    value1=document.getElementById("textipt").value;
     element=document.createElement("div");

    if(value1!==""){
        text1=document.createTextNode(value1);
        element.appendChild(text1);
        mydiv=document.getElementById("mydiv1");
        mydiv.appendChild(element);
        element.id="mydiv"
        element.style.width="10%";
        element.style.height="auto";
        element.style.backgroundColor="white";
        element.style.color="black";
        element.style.marginTop="3%";
        element.style.borderRadius="50px";
        element.style.marginRight="90%";
        element.style.textAlign="center";
        element.style.fontFamily= "Arial, Helvetica, sans-serif";
        element.style.paddingTop="5px";
        document.getElementById("textipt").value="";
    }
}

